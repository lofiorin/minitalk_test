#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	char	*s_cpy;

	s_cpy = (char *)s;
	while (n-- > 0)
	{
		*s_cpy = c;
		s_cpy++;
	}
	return (s);
}
